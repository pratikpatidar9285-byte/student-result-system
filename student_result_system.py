import pandas as pd
import matplotlib.pyplot as plt

# Load data
data = pd.read_csv("data.csv")

# Calculate total and percentage
data["Total"] = data.iloc[:, 1:].sum(axis=1)
data["Percentage"] = data["Total"] / 5

# Find topper
topper = data.loc[data["Percentage"].idxmax()]
print("Topper:", topper["Name"])

# Average marks
print("Average Marks:", data["Percentage"].mean())

# Plot graph
data.plot(x="Name", y="Percentage", kind="bar")
plt.title("Student Percentage")
plt.ylabel("Percentage")
plt.savefig("output.png")
plt.show()